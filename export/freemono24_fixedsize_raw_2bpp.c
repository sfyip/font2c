#if ENABLE_UTF8
static const utf8_t utf8_map[] = {};

// BINARY SEARCH USING ITERATIVE CALL
int binary_search(const utf8_t arr[], int l, int r, utf8_t x)
{
    while (l <= r)
    {
        int m = l + (r-l)/2;
        if (arr[m] == x)
            return m;
        else if (arr[m] < x)
            l = m + 1;
        else
            r = m - 1;
    }

    return -1;
}
#endif

static bool freemono24_lookup(utf8_t c, font_symbol_t *sym)
{
    // --- EDIT BEGIN
    if(c >= '0' && c<= '9')
    {
        sym->bmp_index = (c-'0') * sizeof(freemono24_bmp[0]);
    }
    else if(c == ':')
    {
        sym->bmp_index = 10 * sizeof(freemono24_bmp[0]);
    }
    else if(c >= 'A' && c <= 'Z')
    {
        sym->bmp_index = ((c-'A')+10+1) * sizeof(freemono24_bmp[0]);
    }
    else if(c >= 'a' && c <= 'z')
    {
        sym->bmp_index = ((c-'a')+10+1+26) * sizeof(freemono24_bmp[0]);
    }
    else
    {
#if ENABLE_UTF8
        // Search utf8_map to find the character ?
        int index = binary_search(utf8_map, 0, sizeof(utf8_map)/sizeof(utf8_map[0]) - 1, c);

        if(index == -1)
        {
            return false;
        }

        t = &freemono24_table[63+index];
#else
        return false;
#endif
    }
    // EDIT END ---

    return true;
}

